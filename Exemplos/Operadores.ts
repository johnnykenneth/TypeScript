// AGORA VEJA COM ORDEM DE PRECENDENCIA

console.log( (2 + 3) * 4 );
// Primeiro faz (2 + 3) = 5
// Depois 5 * 4 = 20

console.log( 2 + 3 * 4 );
// Primeiro 3 * 4 = 12
// Depois 2 + 12 = 14

console.log( 10 + 6 / 2 );
// Primeiro 6 / 2 = 3
// Depois 10 + 3 = 13

console.log( 2 * 3 ** 2 );
// Primeiro 3 ** 2 = 9
// Depois 2 * 9 = 18

